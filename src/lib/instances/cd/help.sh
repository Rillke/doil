#!/bin/bash

cat <<-EOF
NAME
  doil instances:cd - switches the active directory
  root folder

SYNOPSIS
  doil instances:cd <instance>

ALIAS
  doil cd

DESCRIPTION
  This command switches the active directory to the instances
  root folder.

EXAMPLE:
  doil instances:cd ilias
  doil cd ilias

OPTIONS
  -h|--help       displays this help message
EOF